    public static Calendar getSalah(Calendar date, int alterMinutes, boolean hanafi, PrayerType salah) {
        try {
            Calendar calendar = (Calendar) date.clone();
            String rawSalah = getRawSalah(calendar, hanafi, salah);
            String[] splitted = rawSalah.split(":");
            calendar.set(Calendar.HOUR_OF_DAY, Integer.valueOf(splitted[0]));
            calendar.set(Calendar.MINUTE, Integer.valueOf(splitted[1]));
            calendar.add(Calendar.MINUTE, alterMinutes);
            calendar.set(Calendar.SECOND, 0);
            calendar.set(Calendar.MILLISECOND, 0);
            alterDST(calendar);
            return calendar;
        } catch (Exception e) {
            return null;
        }
    }

