    private static String getRawDay(Calendar date, boolean hanafi) {
        int day = date.get(Calendar.DAY_OF_MONTH);
        String dayText = day < 10 ? "0" + day : String.valueOf(day);
        String month = getRawMonth(date, hanafi);
        String[] monthSplit = month.split("&");
        for (String days : monthSplit) {
            if (days.startsWith(dayText + ";"))
                return days;
        }
        return null;
    }

    private static String getRawSalah(Calendar date, boolean hanafi, PrayerType salah) {
        String day = getRawDay(date, hanafi);
        if (day != null) {
            String[] daySplit = day.split(";");
            return daySplit[salah.getIndexNo()];
        }
        return null;
    }

